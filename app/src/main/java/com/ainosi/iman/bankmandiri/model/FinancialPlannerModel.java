package com.ainosi.iman.bankmandiri.model;

public class FinancialPlannerModel {
    private String accountnumber, date, saving_a, saving_b, saving_c, total;

    public String getAccountnumber() {
        return accountnumber;
    }

    public void setAccountnumber(String accountnumber) {
        this.accountnumber = accountnumber;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSaving_a() {
        return saving_a;
    }

    public void setSaving_a(String saving_a) {
        this.saving_a = saving_a;
    }

    public String getSaving_b() {
        return saving_b;
    }

    public void setSaving_b(String saving_b) {
        this.saving_b = saving_b;
    }

    public String getSaving_c() {
        return saving_c;
    }

    public void setSaving_c(String saving_c) {
        this.saving_c = saving_c;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
